
package a3;

//class to represent window
public class Window
{
    double xmin;
    double ymin;
    double xmax;
    double ymax;
      
    Window(double xmin, double xmax, double ymin, double ymax)
    {
        this.xmin = xmin;
        this.xmax = xmax;
        this.ymin = ymin;
        this.ymax = ymax;
    }
}
